public class Stereotype {
    String typeName;
    String food;
    String drink;
    String music;
    String hangout;
    String hobby;
    String style;

    public Stereotype() {

    }

    public String getTypeName() {
        return typeName;
    }

    public String getFood() {
        return food;
    }

    public String getDrink() {
        return drink;
    }

    public String getMusic() {
        return music;
    }

    public String getHangout() {
        return hangout;
    }

    public String getHobby() {
        return hobby;
    }

    public String getStyle() {
        return style;
    }
}
